#!/usr/bin/env bash
# Phase 45 WS4 — Capacitor build with a LAN-reachable API URL for WebView testing.
# Requires android/ or ios/ native project (npm run cap:add:android once on a dev machine).
# Usage (repo root): ./scripts/cap-lan-build.sh [--api-port 8080]
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
UI="$ROOT/ui"
API_PORT=8080

while [[ $# -gt 0 ]]; do
  case "$1" in
    --api-port) API_PORT="$2"; shift 2 ;;
    -h|--help)
      echo "Usage: scripts/cap-lan-build.sh [--api-port 8080]"
      exit 0
      ;;
    *) echo "unknown option: $1" >&2; exit 1 ;;
  esac
done

LAN_IP="$(hostname -I 2>/dev/null | awk '{print $1}')"
if [[ -z "${LAN_IP:-}" ]]; then
  echo "error: could not detect LAN IP" >&2
  exit 1
fi

API_URL="${VITE_API_URL:-http://${LAN_IP}:${API_PORT}}"
ENV_LOCAL="$UI/.env.capacitor.local"

cat > "$ENV_LOCAL" <<EOF
# Generated by scripts/cap-lan-build.sh — gitignored; safe to delete.
VITE_API_URL=${API_URL}
EOF

echo "Wrote ${ENV_LOCAL} with VITE_API_URL=${API_URL}"
echo "Remember: API CORS_ORIGIN must allow the WebView origin (Capacitor uses https://localhost by default)."

cd "$UI"
npm run cap:sync

echo "Next: cd ui && npm run cap:open:android  (or cap:open:ios on macOS)"
